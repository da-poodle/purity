% pchar(Char).
%
% Char is a character in the pchar domain.
pchar(pchar(C)) :-
    ch(C,C,=).

% pdif_char(CharA, CharB, IsDifferent).
%
% CharA and CharB are characters in the pchar domain.
% IsDifferent is true if CharA and CharB are different.
%
% A = CharA
% B = CharB
% R = IsDifferent
%
pdif_char(A,B,R) :-
    ch(A,B,C),
    pdif_char_(C,R).

pdif_char_(=,false).
pdif_char_(<,true).
pdif_char_(>,true).

